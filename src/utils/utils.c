#include "cub3d.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	return (s1[i] - s2[i]);
}

int	max_width(char **map)
{
	int		i;
	int		j;
	int		max;

	max = 0;
	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
			j++;
		if (j > max)
			max = j;
		i++;
	}
	return (max);
}

int	max_height(char **map)
{
	int		i;

	i = 0;
	while (map[i])
		i++;
	return (i);
}